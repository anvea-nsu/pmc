;==============================================================
; UART с прерываниями — приём и обратная передача строки
; Микроконтроллер: ATmega168A
; Тактовая частота: 16 МГц
; Скорость UART:   9600 бод
;
; Задача:
;   1. Принять строку из 10 символов (последний — знак конца строки '\0')
;   2. Сохранить строку в SRAM (буфер rx_buf)
;   3. Отправить строку обратно в терминал
;
; Прерывания:
;   USART_RX — срабатывает при получении каждого байта
;   USART_UDRE — срабатывает, когда регистр данных пуст (передача)
;==============================================================

; ============================================================
; Константы
; ============================================================
.equ UBRR_VAL   = 103       ; 16000000 / (16 * 9600) - 1 = 103
.equ BUF_SIZE   = 10        ; длина буфера (9 символов + '\0')
.equ EOL        = 0x00      ; знак конца строки — нулевой байт

; ============================================================
; Адреса буферов в SRAM
; Буфер начинается с 0x0100 (начало SRAM у ATmega168A)
; ============================================================
.equ rx_buf     = 0x0100    ; буфер приёма (10 байт: 0x0100–0x0109)
.equ tx_buf     = 0x010A    ; буфер передачи (10 байт: 0x010A–0x0113)

; ============================================================
; Регистры общего назначения (псевдонимы)
; ============================================================
; r16 — временный регистр (общий)
; r17 — временный регистр (в ISR)
; r18 — счётчик принятых байт (rx_count)
; r19 — счётчик переданных байт (tx_count)
; r20 — флаги: бит 0 = строка принята (rx_done), бит 1 = передача идёт

; ============================================================
; Таблица векторов прерываний ATmega168A
; Каждый вектор занимает 2 слова (4 байта), поэтому шаг = 0x0002 в словах
; ============================================================
.org 0x0000
    rjmp    RESET               ; вектор сброса

.org 0x0002
    reti                        ; INT0   — не используется

.org 0x0004
    reti                        ; INT1   — не используется

.org 0x0006
    reti                        ; PCINT0 — не используется

.org 0x0008
    reti                        ; PCINT1 — не используется

.org 0x000A
    reti                        ; PCINT2 — не используется

.org 0x000C
    reti                        ; WDT    — не используется

.org 0x000E
    reti                        ; TIMER2 COMPA — не используется

.org 0x0010
    reti                        ; TIMER2 COMPB — не используется

.org 0x0012
    reti                        ; TIMER2 OVF   — не используется

.org 0x0014
    reti                        ; TIMER1 CAPT  — не используется

.org 0x0016
    reti                        ; TIMER1 COMPA — не используется

.org 0x0018
    reti                        ; TIMER1 COMPB — не используется

.org 0x001A
    reti                        ; TIMER1 OVF   — не используется

.org 0x001C
    reti                        ; TIMER0 COMPA — не используется

.org 0x001E
    reti                        ; TIMER0 COMPB — не используется

.org 0x0020
    reti                        ; TIMER0 OVF   — не используется

.org 0x0022
    reti                        ; SPI STC — не используется

.org 0x0024
    rjmp    ISR_USART_RX        ; USART RX Complete — ИСПОЛЬЗУЕТСЯ

.org 0x0026
    rjmp    ISR_USART_UDRE      ; USART Data Register Empty — ИСПОЛЬЗУЕТСЯ

.org 0x0028
    reti                        ; USART TX Complete — не используется


; ============================================================
; RESET — инициализация
; ============================================================
.org 0x002A
RESET:
    ; ── Инициализация стека ──────────────────────────────────
    ; RAMEND у ATmega168A = 0x04FF
    ldi     r16, HIGH(RAMEND)
    out     SPH, r16
    ldi     r16, LOW(RAMEND)
    out     SPL, r16

    ; ── Обнуление рабочих регистров ──────────────────────────
    clr     r18                 ; rx_count = 0
    clr     r19                 ; tx_count = 0
    clr     r20                 ; флаги = 0

    ; ── Инициализация UART ───────────────────────────────────
    rcall   UART_Init

    ; ── Разрешить глобальные прерывания ──────────────────────
    sei

; ── Главный цикл ─────────────────────────────────────────────
; Ждём, пока ISR_USART_RX не установит бит 0 в r20 (rx_done).
; Когда строка принята — копируем rx_buf → tx_buf и запускаем передачу.
MAIN_LOOP:
    sbrs    r20, 0              ; проверяем флаг rx_done (бит 0)
    rjmp    MAIN_LOOP           ; ещё не принято — ждём

    ; Строка принята. Сбрасываем флаг.
    cbr     r20, (1<<0)

    ; Копируем rx_buf в tx_buf
    rcall   CopyRxToTx

    ; Запускаем передачу: обнуляем счётчик и разрешаем прерывание UDRE
    clr     r19                 ; tx_count = 0
    lds     r16, UCSR0B
    ori     r16, (1<<UDRIE0)    ; разрешить прерывание Data Register Empty
    sts     UCSR0B, r16

    ; Ждём окончания передачи (бит 1 флагов сбрасывается в ISR_USART_UDRE)
WAIT_TX_DONE:
    sbrc    r20, 1              ; если бит 1 = 1, передача ещё идёт
    rjmp    WAIT_TX_DONE

    ; Готовы принять следующую строку — обнуляем счётчик приёма
    clr     r18                 ; rx_count = 0

    rjmp    MAIN_LOOP


; ============================================================
; UART_Init — настройка UART с прерываниями
; ============================================================
UART_Init:
    ; Скорость: UBRR = 103 (9600 бод при 16 МГц)
    ldi     r16, HIGH(UBRR_VAL)
    sts     UBRR0H, r16
    ldi     r16, LOW(UBRR_VAL)
    sts     UBRR0L, r16

    ; UCSR0B: разрешить приём (RXEN0), передачу (TXEN0),
    ;         прерывание по завершению приёма (RXCIE0)
    ; UDRIE0 пока не включаем — включим только перед отправкой
    ; Биты: RXCIE0=7, TXCIE0=6, UDRIE0=5, RXEN0=4, TXEN0=3
    ldi     r16, (1<<RXCIE0)|(1<<RXEN0)|(1<<TXEN0)
    sts     UCSR0B, r16

    ; UCSR0C: формат 8N1 (UCSZ01=1, UCSZ00=1)
    ldi     r16, (1<<UCSZ01)|(1<<UCSZ00)
    sts     UCSR0C, r16

    ret


; ============================================================
; CopyRxToTx — копирует 10 байт из rx_buf в tx_buf
; Использует Z-указатель (r30:r31) и X-указатель (r26:r27)
; ============================================================
CopyRxToTx:
    ; Z → rx_buf (источник)
    ldi     r30, LOW(rx_buf)
    ldi     r31, HIGH(rx_buf)
    ; X → tx_buf (приёмник)
    ldi     r26, LOW(tx_buf)
    ldi     r27, HIGH(tx_buf)

    ldi     r16, BUF_SIZE       ; счётчик = 10
COPY_LOOP:
    ld      r17, Z+             ; читаем байт из rx_buf, инкремент Z
    st      X+, r17             ; пишем в tx_buf, инкремент X
    dec     r16
    brne    COPY_LOOP

    ret


; ============================================================
; ISR_USART_RX — прерывание: принят очередной байт
;
; Что делает:
;   1. Сохраняет принятый байт в rx_buf[rx_count]
;   2. Увеличивает rx_count
;   3. Если принят символ '\0' или буфер заполнен (10 байт) —
;      устанавливает флаг rx_done (бит 0 r20)
;
; Сохраняем SREG, так как документация требует этого в ISR.
; ============================================================
ISR_USART_RX:
    ; Сохранить контекст
    push    r16
    push    r17
    in      r17, SREG
    push    r17

    ; Читаем принятый байт из UDR0
    lds     r16, UDR0

    ; Сохраняем байт в rx_buf + rx_count
    ; Для вычисления адреса: Z = rx_buf + rx_count
    ldi     r30, LOW(rx_buf)
    ldi     r31, HIGH(rx_buf)
    add     r30, r18            ; r30 += rx_count
    brcc    NO_CARRY_RX
    inc     r31                 ; перенос в старший байт адреса
NO_CARRY_RX:
    st      Z, r16              ; сохраняем байт в буфер

    ; Увеличиваем счётчик
    inc     r18                 ; rx_count++

    ; Проверяем условие конца строки: EOL или буфер заполнен
    cpi     r16, EOL            ; принят нулевой байт?
    breq    RX_DONE
    cpi     r18, BUF_SIZE       ; rx_count == 10?
    brsh    RX_DONE

    rjmp    RX_EXIT

RX_DONE:
    ; Убеждаемся, что последний байт в буфере = '\0'
    ; (нужно, если строка заполнена без явного '\0')
    ldi     r30, LOW(rx_buf + BUF_SIZE - 1)
    ldi     r31, HIGH(rx_buf + BUF_SIZE - 1)
    ldi     r16, EOL
    st      Z, r16

    ; Устанавливаем флаг rx_done и флаг "идёт передача"
    sbr     r20, (1<<0)|(1<<1)

RX_EXIT:
    ; Восстановить контекст
    pop     r17
    out     SREG, r17
    pop     r17
    pop     r16
    reti


; ============================================================
; ISR_USART_UDRE — прерывание: регистр данных пуст, можно писать
;
; Что делает:
;   1. Берёт байт из tx_buf[tx_count] и записывает в UDR0
;   2. Увеличивает tx_count
;   3. Когда все 10 байт отправлены — запрещает прерывание UDRE
;      и сбрасывает флаг "идёт передача" (бит 1 r20)
; ============================================================
ISR_USART_UDRE:
    ; Сохранить контекст
    push    r16
    push    r17
    in      r17, SREG
    push    r17

    ; Проверяем: все байты отправлены?
    cpi     r19, BUF_SIZE
    brsh    TX_DONE

    ; Берём байт из tx_buf + tx_count
    ldi     r30, LOW(tx_buf)
    ldi     r31, HIGH(tx_buf)
    add     r30, r19            ; r30 += tx_count
    brcc    NO_CARRY_TX
    inc     r31
NO_CARRY_TX:
    ld      r16, Z              ; загружаем байт для отправки

    ; Записываем в UDR0 — начнётся передача
    sts     UDR0, r16

    ; Увеличиваем счётчик
    inc     r19                 ; tx_count++

    rjmp    TX_EXIT

TX_DONE:
    ; Все байты отправлены. Запрещаем прерывание UDRE.
    lds     r16, UCSR0B
    cbr     r16, (1<<UDRIE0)
    sts     UCSR0B, r16

    ; Сбрасываем флаг "передача идёт" (бит 1)
    cbr     r20, (1<<1)

TX_EXIT:
    ; Восстановить контекст
    pop     r17
    out     SREG, r17
    pop     r17
    pop     r16
    reti
